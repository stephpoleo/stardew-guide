export function useT(lang) {
  return useCallback(
    (path, vars = {}) => {
      const parts = path.split(".");
      let node = I18N[lang];
      for (const p of parts) node = node && node[p];
      if (typeof node !== "string") return path;
      return node.replace(/\{(\w+)\}/g, (_, k) => (vars[k] != null ? String(vars[k]) : ""));
    },
    [lang],
  );
}
